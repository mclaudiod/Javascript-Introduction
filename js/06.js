// Math Object

let result;

result = Math.PI;
result = Math.round(2.5); // Rounds number
result = Math.ceil(2.1); // Always rounds up
result = Math.floor(2.9); // Always rounds down
result = Math.sqrt(144); // Square root
result = Math.abs(-200); //Transforms negative into positive
result = Math.min(3, 5, 1, 8, 2, 10); // Picks the lowest number from a list
result = Math.max(3, 5, 1, 8, 2, 10); // Picks the largest number from a list
result = Math.random; // Gets a random number lower than 1
result = Math.floor(Math.random * 30);

